package com.company;

public class Point {
    private int squareX;
    private int squareY;

    public Point(int squareX, int squareY) {
        this.squareX = squareX;
        this.squareY = squareY;
    }

    public int getSquareX() {
        return squareX;
    }

    public int getSquareY() {
        return squareY;
    }
}
